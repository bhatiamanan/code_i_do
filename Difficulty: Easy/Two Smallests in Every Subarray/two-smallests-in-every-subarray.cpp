class Solution {
  public:
    int pairWithMaxSum(vector<int>& arr) {
        // code here
        int l=arr.size();
        if(l==1) return -1;
        int maxi=0;
        for(int i=0;i<l-1;i++) maxi=max(maxi,arr[i]+arr[i+1]);
        return maxi;
    }
};

