class Solution {
  public:
    int kthMissing(vector<int> &arr, int k) {
        // Your code goes here
        int n = arr.size();
        int missingCount = 0, lastNum = 0;

        for (int i = 0; i < n; i++) {
            int currentMissing = arr[i] - lastNum - 1;
            if (k <= missingCount + currentMissing) return lastNum + k - missingCount;
            missingCount += currentMissing;
            lastNum = arr[i];
        }
        // If kth missing is beyond the last element of the array
        return lastNum + (k - missingCount);
    }
};

