class Solution{
    public:
    long long findMinDiff(vector<long long> a, long long n, long long m){
    //code
    sort(a.begin(), a.end());
    long long minSum = INT_MAX;


    //Slide m size of window till end
    int left = 0, right = m - 1;


    while(right < n){
        long long sum = a[right] - a[left];
        if(sum < minSum) minSum = sum;
        left++;
        right++;
    }
    return minSum;
    }   
};

